# normal way to declare variables
x = 10

#  we can also declare them with binary
x = 0b10
print(x)

# bin() will tell us the binary representation of a number
print(bin(10))

# hexadecimle
x = 0x12c
print(x)

# or we can find 
print(hex(10))

# complex numbers take the form a + bi
# j is an imaginary number
x = 1 + 2j
print(x)